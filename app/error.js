"use client";

// error boundary must be client component

// error get error obj and function to reset the error boundary
// this convention can place in nested component
// only rendering errors will be caught right here

// error boundary doesnt catch error that might happen in the root layout
export default function Error({ error, reset }) {
  return (
    <main className="flex justify-center items-center flex-col gap-6">
      <h1 className="text-3xl font-semibold">Something went wrong!</h1>
      <p className="text-lg">{error.message}</p>

      <button
        className="inline-block bg-accent-500 text-primary-800 px-6 py-3 text-lg"
        onClick={reset}
      >
        Try again
      </button>
    </main>
  );
}
